package com.novo.model;

/**
 *
 * @author ggutierrez
 */
public class TAjuste {
    
    private String codigo;
    private String descripcion;
    private String idCodigoAjuste;
    
    public TAjuste() {
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getIdCodigoAjuste() {
        return idCodigoAjuste;
    }

    public void setIdCodigoAjuste(String idCodigoAjuste) {
        this.idCodigoAjuste = idCodigoAjuste;
    }
    
    
    
}
