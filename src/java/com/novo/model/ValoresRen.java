package com.novo.model;

/**
 *
 * @author rgomez
 */
public class ValoresRen {
    
    private String rutaOrigen;
    private String rutaDestino;
    private String host;
    private String usuario;
    private String nombreRenovacion;

    public String getRutaOrigen() {
        return rutaOrigen;
    }

    public void setRutaOrigen(String rutaOrigen) {
        this.rutaOrigen = rutaOrigen;
    }

    public String getRutaDestino() {
        return rutaDestino;
    }

    public void setRutaDestino(String rutaDestino) {
        this.rutaDestino = rutaDestino;
    }

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getNombreRenovacion() {
        return nombreRenovacion;
    }

    public void setNombreRenovacion(String nombreRenovacion) {
        this.nombreRenovacion = nombreRenovacion;
    }
    
    
    
}
